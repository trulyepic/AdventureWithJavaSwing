package model;

public class Player {
	
	private int hp;
	private SuperWeapon currentWeapon;
	
	
	
	
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	public SuperWeapon getCurrentWeapon() {
		return currentWeapon;
	}
	public void setCurrentWeapon(SuperWeapon currentWeapon) {
		this.currentWeapon = currentWeapon;
	}
	
	
	

}
